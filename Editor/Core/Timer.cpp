#include <stdafx.h>
#include "Timer.h"

///////////////////////////////////////////////////////////////////////////////
Timer::Timer()
{
}

///////////////////////////////////////////////////////////////////////////////
Timer::Timer(std::function<void()> fn)
{
	SetCallback(fn);
}

///////////////////////////////////////////////////////////////////////////////
Timer::Timer(const Timer& rhs)
{
	callback_ = rhs.callback_;
}

///////////////////////////////////////////////////////////////////////////////
Timer& Timer::operator= (const Timer& rhs)
{
	if (this != &rhs)
	{
		callback_ = rhs.callback_;
	}

	return *this;
}

///////////////////////////////////////////////////////////////////////////////
void Timer::Notify()
{
	callback_();
}

///////////////////////////////////////////////////////////////////////////////
void Timer::SetCallback(std::function<void()> fn)
{
	callback_ = fn;
}

///////////////////////////////////////////////////////////////////////////////